<p>In collaboration with Dr. Sean Coughlin, scholar of ancient Greek science at the Humboldt-
Universität zu Berlin, I reconstructed the Mendesian, a perfume that served as the olfactory
emblem of ancient Egypt in late antiquity, hence its nickname, “the Egyptian”. The perfume
recipe unfortunately did not survive in the Egyptian sources, however, numerous Greek writers
praise its scent, calling it luxurious and expensive. The perfume was already known in Greece as
early as the 4 th c. BCE, and it was then known as one of the costliest and most desired perfumes
available. The popularity of the fragrance quickly spread throughout the ancient world and
remained sought after for hundreds of years. Our aim was to replicate ancient methods for
producing the Mendesian perfume in light of the most recent written, archaeological and
botanical evidence. We regarded the Mendesian as a case study for understanding the knowledge
transfer of ancient Egyptian perfume recipes from pharaonic Egypt to Greek and Roman authors.
Our starting point were two previously unnoticed recipes in a 7 th c. Greek medical compilation,
which Coughlin identified as authentic and likely originating in a much earlier source. Using
these recipes, we then took an experimental approach to re-discovering and replicating the
techniques they involved. First, we established a range of experimental conditions to test by
cross-referencing the recipes’ ingredients with materials mentioned in earlier Egyptian, Greek
and Latin sources, as well as with archaeological evidence and recent identifications of ancient
botanical terms. Then, as an initial study, we performed experiments with some plausible
candidate conditions. The result is the first contemporary experimental study of the production of
Mendesian perfume based on ancient recipes. Our experiments produced an extremely pleasant
scent with a spicy base note of freshly ground myrrh and cinnamon, accompanied by a hint of
sweetness. As we smelled the Mendesian for the first time, the words ‘elegant’ and ‘luxurious’
came to our minds, reminding us of Pliny calling it “highly praised” and “very famous”, and
Aetius of Amida stating that it is “very softening and relaxing for bodies”. All of a sudden,
Theophrastus’ comment started making sense: “This perfume and the Egyptian take a great deal
of work, since the mixture contains so many and such expensive ingredients”. Our reconstruction
was exhibited at the National Geographic Museum, Washington D.C. between March 1, 2019
and September 15, 2019 in the framework of the exhibition Queens of Egypt.</p></div>
